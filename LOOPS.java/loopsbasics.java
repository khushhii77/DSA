public class loopsbasics {
    public static void main(String args[]) {
//for loop....   for(initialisation; condition; updation) {operations}
for(int counter = 0;  counter < 5;  counter= counter + 1) {
    System.out.println("hey....");
}


for(int  i = 0 ; i<11 ; i++){
        System.out.println(i);
    }

//while loop...    initialisation;  while(condition) {operations;  updation}
int b=11;
 while (b<21){
    System.out.println(b);
    b++;
 }

//do while....   initialisation;  do{operation; updation;}  while(condition);
int a=21;
do{
     System.out.println(a);   
     a++;
} while(a<31);


//diff. b/w "while" and "do while" :-
// in while, if conditions is not followed then it will not print anything.
// in do while, if conditions is whether true or false then atleast one line will print bcz "do while" means 1st do then conditions will see..




}
}
 
    

