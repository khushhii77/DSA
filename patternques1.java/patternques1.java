public class patternques1 {
    public static void main(String args[]) {

int m=4;

for(int i=1; i<=m; i++){
    for(int j=1; j<=i; j++){  
       System.out.print("*");
    }
    System.out.println();
    }
    
    // System.out.println("\n");

for(int i=m; i>=1; i--){
    for(int j=1; j<=i; j++){
        System.out.print("*");
    }
     System.out.println();
    }
    
    System.out.println("\n");
    
    }
    }


    